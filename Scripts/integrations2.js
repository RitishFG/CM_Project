let a = document.querySelectorAll("#filter li");
a.forEach(function (el) {
  el.addEventListener("click", myFilter);
});
function myFilter(event) {
  let selection = event.target.innerText;
  if (selection == "All") {
    display(items);
  } else if (selection == "Advertising") {
    let res = items.filter(function (el) {
      return el.fl == "ad";
    });
    display(res);
  } else if (selection == "Billing") {
    let res = items.filter(function (el) {
      return el.fl == "b";
    });
    display(res);
  } else if (selection == "CMS") {
    let res = items.filter(function (el) {
      return el.fl == "cm";
    });
    display(res);
  } else if (selection == "Content & Video") {
    let res = items.filter(function (el) {
      return el.fl == "cv";
    });
    display(res);
  } else if (selection == "E-Commerce") {
    let res = items.filter(function (el) {
      return el.fl == "ec";
    });
    display(res);
  } else if (selection == "Email testing") {
    let res = items.filter(function (el) {
      return el.fl == "et";
    });
    display(res);
  } else if (selection == "Most popular") {
    let res = items.filter(function (el) {
      return el.fl == "mp";
    });
    display(res);
  } else if (selection == "Social") {
    let res = items.filter(function (el) {
      return el.fl == "s";
    });
    display(res);
  } else if (selection == "Surveys") {
    let res = items.filter(function (el) {
      return el.fl == "su";
    });
    display(res);
  }
}

display(items);

function display(items) {
  let mn = document.querySelector("#cards");
  mn.innerHTML = "";
  items.forEach(function (el) {
    let d = document.createElement("div");

    let i = document.createElement("img");
    i.src = el.image;
    i.style.backgroundColor = el.bg;

    let h = document.createElement("h4");
    h.innerText = el.title;

    let p = document.createElement("p");
    p.innerText = el.desc;

    d.append(i, h, p);
    mn.append(d);
  });
}
